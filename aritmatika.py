
#Belajar operator aritmatika
nilai_pertama = int(input("Masukkan nilai pertama : "))
nilai_kedua = int(input("Masukkan nilai kedua : "))

print("Hasil penjumlahan = ",nilai_pertama + nilai_kedua)
print("Hasil pengurangan = ",nilai_pertama - nilai_kedua)
print("Hasil perkalian = ",nilai_pertama * nilai_kedua)
print("Hasil pembagian = ",nilai_pertama / nilai_kedua)


